export function getWeekPlan(
  goal: Goal,
  location: TrainingLocation,
  level: Level,
  frequency: number
): DayPlan[] {
  const key = `${goal}_${location}_${level}`
  const sessions = MENUS[key] ?? []
  const trainingDayIndices = TRAINING_DAYS[frequency] ?? []
  return DAYS.map((day, idx) => {
    const sessionIdx = trainingDayIndices.indexOf(idx)
    if (sessionIdx === -1) return { day, isRest: true, session: null }
    return { day, isRest: false, session: sessions[sessionIdx] ?? null }
  })
}
